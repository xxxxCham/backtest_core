        # short entries
        df.loc[short_mask, "bb_stop_short"] = close[short_mask] + stop_atr_mult * atr[short_mask]
        df.loc[short_mask, "bb_tp_short"] = close[short_mask] - final_tp_atr_mult * atr[short_mask]
        signals.iloc[:warmup] = 0.0
        return signals
